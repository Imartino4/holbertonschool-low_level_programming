#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - return the addition two integers
 * @a: int to add
 * @b: int to add
 *
 * Description -
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference of two integers
 * @a: int
 * @b: int
 *
 * Description -
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - return the product of two integers
 * @a: int to multipicate
 * @b: int to multiplicate
 *
 * Description -
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the division of two integers
 * @a: int
 * @b: int
 *
 * Description -
 * Return: division a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder of the division
 * @a: int
 * @b: int
 *
 * Description -
 * Return: reminder a/b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
